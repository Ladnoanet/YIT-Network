const PHRASE_PATTERNS = [
  /иди\s+на\s+х[уy]/i,
  /пошёл\s+на\s+х[уy]/i,
  /пошел\s+на\s+х[уy]/i,
  /[её]б\s+твою\s+мать/i,
  /твою\s+мать/i,
  /в\s+ж[оо]пу/i,
  /в\s+п[иеё][з3]д/i,
  /нах[уy][йяеи]/i,
];

function normalize(text) {
  let t = text.toLowerCase();
  t = t.replace(/([а-яёa-z])[_.\-\/\\| ]+([а-яёa-z])/gi, '$1$2');
  t = t.replace(/([а-яёa-z])\1{2,}/gi, '$1');
  t = t.replace(/ё/g, 'е').replace(/[ъь]/g, '');
  t = t.split('').map(c => VISUAL_MAP[c] || c).join('');
  const multi = ['shch','sch','sh','ch','zh','kh','ts','ya','yu','yo','iy'];
  for (const s of multi) t = t.replaceAll(s, TRANSLIT_MAP[s]);
  t = t.split('').map(c => TRANSLIT_MAP[c] || c).join('');
  t = t.replace(/[^а-яa-z]/g, '');
  return t;
}

function levenshtein(a, b) {
  const m = a.length, n = b.length;
  const dp = Array.from({length: m+1}, (_, i) =>
    Array.from({length: n+1}, (_, j) => i === 0 ? j : j === 0 ? i : 0)
  );
  for (let i = 1; i <= m; i++)
    for (let j = 1; j <= n; j++)
      dp[i][j] = a[i-1] === b[j-1]
        ? dp[i-1][j-1]
        : 1 + Math.min(dp[i-1][j], dp[i][j-1], dp[i-1][j-1]);
  return dp[m][n];
}

function hasProfanity(text) {
  for (const re of PHRASE_PATTERNS)
    if (re.test(text)) return true;

  const norm = normalize(text);

  for (const root of BAN_ROOTS)
    if (norm.includes(root)) return true;

  for (const word of norm.split(/\s+/).filter(w => w.length >= 3)) {
    for (const root of BAN_ROOTS) {
      if (Math.abs(word.length - root.length) <= 2 && levenshtein(word, root) <= 1)
        return true;
    }
  }

  return false;
}


